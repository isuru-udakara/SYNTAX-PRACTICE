// practice swith statements 

let grade= "A";

switch(grade){
    case "A":
        console.log("Ant");
        break;
    case "B":
        console.log("Bear");
        break;
    case "C":
        console.log("Crane");
        break;
    case "D":
        console.log("Dragon");
        break;
    case "E":
        console.log("Elephant");
        break;
    default: 
        console.log("Tiger");
}